
class Percept:
    def __init__(self, north=0, east=0, south=0, west=0):
        self.north = north
        self.east = east
        self.south = south
        self.west = west

    def toString(self):
        print("North Percept: ", self.north)
        print("East Percept: ", self.north)
        print("South Percept: ", self.north)
        print("West Percept: ", self.north)
