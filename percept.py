
class Percept:
    def __init__(self):
        self.north = None
        self.east = None
        self.south = None
        self.west = None

    def toString(self):
        print("North Percept: ", self.north)
        print("East Percept: ", self.north)
        print("South Percept: ", self.north)
        print("West Percept: ", self.north)
